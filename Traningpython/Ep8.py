# ตัวดำเนินการ & ตัวที่ถูกดำเนินการ
# Operator | Operand

# A + B
# A ,B => Operand
# + => Operator

x = 10 
y = 4

print("ผลบวก =", x+y)
print("ผลลบ =", x-y)
print("ผลคูณ =", x*y)
print("ผลหาร =", x/y)
print("หารปัดเศษ =", x//y)
print("ยกกำลัง =", x**y)
print("หารเอาเศษ =", x%y)